Renderer::Renderer()
{

}

Renderer::~Renderer() {
}

void Renderer::resize(int w, int h) {

    glViewport(0, 0, w, h);
}



void Renderer::render() {


    glClearColor(0.2f, 0.2f, 0.3f, 1.0f);
    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
    draw();
    checkGlError("Renderer::render");
}

